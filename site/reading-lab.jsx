/* ================= РАЗДЕЛ САЙТА ================= */

const RL_DONE_KEY = "mmm_reading_lab_done";

function ReadingLabPage({ role }) {
  const [screen, setScreen] = rlUseState("home");
  const [studentPreview, setStudentPreview] = rlUseState(false);
  const [done, setDone] = rlUseState(() => {
    try { return new Set(JSON.parse(localStorage.getItem(RL_DONE_KEY) || "[]")); } catch (e) { return new Set(); }
  });
  rlUseEffect(() => {
    try { localStorage.setItem(RL_DONE_KEY, JSON.stringify([...done])); } catch (e) { }
  }, [done]);

  const goHome = () => { setScreen("home"); setStudentPreview(false); rlScrollPage(); };
  const openProblem = (id) => { setScreen(id); setStudentPreview(false); rlScrollPage(); };
  const cur = PROBLEMS.find(p => p.id === screen);

  let body;
  if (!cur) {
    body = <LabHome done={done} role={role} open={openProblem} />;
  } else if (role === "teacher" && !studentPreview) {
    body = <TeacherView problem={cur}
      onExit={goHome}
      openAsStudent={() => { setStudentPreview(true); rlScrollPage(); }} />;
  } else {
    const Comp = cur.comp;
    body = (
      <div>
        <div className="topbar">
          <button className="back-btn" onClick={role === "teacher" ? () => { setStudentPreview(false); rlScrollPage(); } : goHome}>← {role === "teacher" ? "К разбору" : "Задачи"}</button>
          <h2>{cur.title}</h2>
        </div>
        <Comp key={cur.id} onComplete={() => {
          setDone(d => { const nd = new Set(d); nd.add(cur.id); return nd; });
          goHome();
        }} />
      </div>
    );
  }

  return (
    <div className="rlab">
      <RL.LabStyles />
      {body}
    </div>
  );
}

window.MMM_READING_LAB = { ReadingLabPage };
